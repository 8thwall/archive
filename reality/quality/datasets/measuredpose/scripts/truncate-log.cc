#include <fcntl.h>
#include <unistd.h>
#include <cxxopts.hpp>
#include <mutex>
#include "c8/c8-log.h"
#include "c8/io/capnp-messages.h"
#include "c8/protolog/api/log-request.capnp.h"
#include "reality/quality/visualization/protolog/capnpbin-reader.h"

using namespace c8;

namespace {
constexpr auto &fcntl_open = open;
constexpr auto &fcntl_close = close;
}

int main(int argc, char *argv[]) {
  cxxopts::Options options(
    "truncate-log", "Slice frames from a logrecord log file.");
  options.add_options()(
    "o,outFile",
    "[Optional] Output file to write to",
    cxxopts::value<std::string>()->default_value(""))(
    "i,inFile",
    "[Required] Input file generated by sensorstream",
    cxxopts::value<std::string>()->default_value(""))(
    "s,startFrame",
    "[Required] Frame number to start truncation",
    cxxopts::value<int>()->default_value("0"))(
    "n,numFrames",
    "[Required] Number of frames to include",
    cxxopts::value<int>()->default_value("-1"));
  auto result = options.parse(argc, argv);

  // Make sure that an input file was specified.
  auto infile = result["inFile"].as<std::string>();
  if (infile.empty()) {
    C8Log(
      "%s\n%s",
      "ground-homography-camera-pos: missing reqiured flag -i (--inFile)",
      "  Use ground-homgraphy-camera-pos --help for more details.");
    return -1;
  }

  std::unique_ptr<CapnpbinReader> reader(CapnpbinReader::open(infile.c_str()));

  if (reader == nullptr) {
    C8Log("ERROR: Couldn't open \"%s\" for read.", infile.c_str());
    return -1;
  }

  auto outfile = result["outFile"].as<std::string>();
  if (outfile.empty()) {
    C8Log("%s", "No --outputFile specified. Quitting without saving.");
    return 0;
  }

  // Open the output file for save.
  int fd = fcntl_open(outfile.c_str(), O_WRONLY | O_CREAT, S_IREAD | S_IWRITE);
  if (fd < 0) {
    C8Log("ERROR: Couldn't open %s for write.", outfile.c_str());
    return -1;
  }

  auto s = result["startFrame"].as<int>();
  auto n = result["numFrames"].as<int>();

  // Read each message from the file.
  int i = -1;
  int wroteFrames = 0;
  std::unique_ptr<MutableRootMessage<LogRecord>> record(new MutableRootMessage<LogRecord>());
  while (reader->read<LogRecord>(record.get())) {
    if (++i < s) {
      record.reset(new MutableRootMessage<LogRecord>());
      continue;
    }

    if (n >=0 && i >= s + n) {
      break;
    }

    // Append the record to the file.
    ++wroteFrames;
    capnp::writeMessageToFd(fd, *(record->message()));
    record.reset(new MutableRootMessage<LogRecord>());
  }

  // Close the file.
  fcntl_close(fd);
  C8Log("Finished writing %d frames to file: %s", wroteFrames, outfile.c_str());

  return 0;
}

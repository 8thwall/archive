module.exports = (sequelize, DataTypes) => {
  const AppEngagementMetric = sequelize.define('AppEngagementMetric', {
    // Records must be unique to combinations of dt x appKey
    dt: {
      type: DataTypes.DATE,
      allowNull: false,
      comment: 'UTC date (format: `YYYY-MM-DD hh:mm:ss`) of activity',
    },
    appKey: {
      type: DataTypes.STRING,
      allowNull: false,
      comment: 'This is a foreign key in the App table',
      references: {
        model: 'Apps',
        key: 'appKey',
      },
    },

    // Records shouldn't exist for day without any page time
    meanPageTimeMs: {
      type: DataTypes.INTEGER,
      allowNull: false,
      comment: 'Daily average duration spent in web page in milliseconds',
    },
    totalSessions: {
      type: DataTypes.BIGINT,
      allowNull: false,
      comment: 'Num console sessions. Denominator of samplingRate.',
    },
    sampledSessions: {
      type: DataTypes.BIGINT,
      allowNull: false,
      comment: 'Num engine sessions sampled by dynamic logging. Numerator of samplingRate.',
    },

  }, {
    // Columns createdAt and updatedAt are autogenerated by sequelize but are not meaningful
    //    for aggregated metrics.
    // Records are unique at the dt,appKey level and refreshed hourly by athena-export lambda
    createdAt: false,
    updatedAt: false,

  })

  AppEngagementMetric.associate = (models) => {
    AppEngagementMetric.belongsTo(models.App, {foreignKey: 'appKey'})
  }
  // Column id is autogenerated by sequelize but not needed for this table.
  // Records are unique at the dt,appKey level and refreshed hourly by athena-export lambda
  AppEngagementMetric.removeAttribute('id')

  return AppEngagementMetric
}
